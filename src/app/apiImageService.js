import axios from "axios";
import { BASE_IMAGE_URL } from "../../src/app/config.js";

const apiImageService = axios.create({
  baseURL: BASE_IMAGE_URL,
});

apiImageService.interceptors.request.use(
  (request) => {
    console.log("Start Request", request);
    return request;
  },
  function (error) {
    console.log("REQUEST ERROR", error);
    return Promise.reject(error);
  }
);

apiImageService.interceptors.response.use(
  (response) => {
    console.log("Response", response);
    return response;
  },
  function (error) {
    console.log("RESPONSE ERROR", error);
    return Promise.reject(error);
  }
);

export default apiImageService;
